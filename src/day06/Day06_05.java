package day06;

public class Day06_05 {

	public static void main(String[] args) {
		
		int sum = 0;
		
		for(int count = 1; count <=10 ; count++) {
			System.out.println("홍길동 를! 했다.");
			System.out.println(count+"번 를! 반복했다.");
		sum= sum + count;
		System.out.println("1~10의 합 : "+sum);
		}

	}

}
